def sincos(angle):
    if angle<0:
        angle=angle+360
    sinval=SinTable[angle]
    cosval=SinTable[450-angle]
    return sinval,cosval
    
    
def ellipsePoly_my(center,axes,angle,arc_start,arc_end,delta):
    #axes.width
    size_a=axes[0]
    #axes.height
    size_b=axes[1]
    #center.x
    cx=center[0]
    #center.y
    cy=center[1]
    prevPt=[INT_MIN,INT_MIN]
    
    while angle<0:
        angle=angle+360
    while angle>360:
        angle=angle-360
    
    if arc_start>arc_end:
        i=arc_start
        arc_start=arc_end
        arc_end=i
        
    while arc_start<0:
        arc_start=arc_start+360
        arc_end=arc_end+360
        
    while arc_end>360:
        arc_end=arc_end-360
        arc_start=arc_start-360
        
    if (arc_end-arc_start)>360:
        arc_start=0
        arc_end=360
        
    alpha,beta=sincos(angle)
    
    pts=[]
    end=arc_end+delta
    #for i in range(start,end,step)
    for i in range(arc_start,end,delta):
        angle=i
        if angle>arc_end:
            angle=arc_end
        if angle<0:
            angle=angle+360
        
        x=size_a*SinTable[450-angle]
        y=size_b*SinTable[angle]
        pt=[]
        #pt.x
        pt.append(round(cx+x*alpha-y*beta))
        pt.append(round(cy+x*beta+y*alpha))
        if pt!=prevPt:
            pts.append(pt)
            prevPt=pt
    
    if len(pts)==1:
        pts.append(pts[0])
        
    return pts
        
def EllipseEx_my(center,axes,angle,arc_start,arc_end):
  
    #Size:(axes.width,axes.height)
    axes[0]=abs(axes[0])
    axes[1]=abs(axes[1])
    delta=1
    pts=ellipsePoly_my(center,axes,angle,arc_start,arc_end,delta)
    
    efc=EFCHandler()
    part_label=efc.Part(pts,coef_num,part_num)
    
    return part_label
 
